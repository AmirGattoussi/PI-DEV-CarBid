package Entities;

/**
 *
 * @author asus
 */
public class Car {
    private int idCar;
    private String brand;

    public Car(int idCar, String brand) {
        this.idCar = idCar;
        this.brand = brand;
    }

    public int getIdCar() {
        return idCar;
    }

    public void setIdCar(int idCar) {
        this.idCar = idCar;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
    
}
